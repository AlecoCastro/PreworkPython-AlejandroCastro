'''Crea un programa que calcule el área de un rectángulo. El usuario debe ingresar la
longitud y el ancho del rectángulo.'''

longitud = float(input("Introduzca longitud del rectangulo: "))
ancho = float(input("Introduzca ancho del rectangulo: "))

área_total = longitud * ancho
print("El área del rectangulo es: ", área_total)